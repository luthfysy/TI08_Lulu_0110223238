# modul perhitungan
import math

def tambah(bil1, bil2):
    hasil = bil1 + bil2
    print(hasil)

def kurang(bil1, bil2):
    hasil = bil1 - bil2
    print(hasil)

def kali(bil1, bil2):
    hasil = bil1 * bil2
    print(hasil)

def bagi(bil1, bil2):
    hasil = bil1 / bil2
    print(hasil)

def pangkat(bil1, bil2):
    hasil = bil1 ** bil2
    print(hasil)

def akar(x):
    return math.sqrt(x)

def count_log(x):
    return math.log(x)

def count_sin(x):
    sin = math.sin(x)
    print(sin)

def count_cos(x):  
    cos = math.tan(x)
    print(cos)

def count_tan(x):
    tan = math.cos(x)
    print(tan)